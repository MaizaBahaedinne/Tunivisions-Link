      <section id="S3">
        <h1>Ressource humaine</h1>
        <h2 id="S31">Section 1 : Procédure de recrutement</h2>

        <br>
        <h3>Article 1 : L’adhésion au club</h3>
        Afin de bien préparer l’intégration des nouveaux membres dans le club, celui-ci a prévu de passer par un entretien ayant lieu au cours de la semaine de recrutement.<br>
        Il faut être agrée par le bureau exécutif statuant à la majorité de tous ses membres.<br>
        Un OC qui dépasse pas les 10 membres avec la supervision du BE prendra en charge la stratégie de recrutement qui s’étale sur 3 étapes :<br>
        <b>1. La gestion des talents :</b><br>
        Il faut bien comprendre les besoins du club et les profils recherchés et établir une stratégie basée sur vos objectifs pour le mandat.<br>
        <b>2. L’attraction et la fidélisation des talents :</b><br>
        Il faut travailler sur la fidélisation des talents reçu au cours des recrutements et mettre les bonnes personnes au bon endroit.<br>
        <b>3. La sélection :</b><br>
        Choisir les membres qui vont intégrer le clubs suite à leurs entretient et établir à chacun son ‘job description’.

        <br>
        <h2 id="S32">Section 2 : Discipline</h2>

        <br>
        <h3>Article 1 : Le respect</h3>
        Toute personne membre se doit de respecter ses collègues, les responsables du bureau et respecter à la lettre les articles du règlement intérieur.<br>
        Tout membre enfreignant une ou plusieurs de règles du règlement est automatiquement convoqué par le conseil disciplinaire. Ce conseil détient le droit de prendre les mesures qu'il juge adéquates.<br>
        
        <br>
        <h3>Article 2 : Les retards</h3>
        Un retard de quinze minutes maximum est toléré pour chaque réunion.<br>
        Tout retard non expliqué dépassant ces quinze minutes est comptabilisé en tant qu’absence.<br>
        Dans les cas de force majeure, tout membre se retrouvant dans l’obligation d’arriver avec un retard dépassant les quinze minutes accordées doit en informer le vice-président administration et finance avant le début de cette dernière et le Vice-Président se garde le droit de juger si ce retard est légitime ou pas.

        <br>
        <h3>Article 3 : Les absences</h3>
        Tout membre dispose d'absence s'élevant à 1/3 de la somme des réunions de son département, de tout événement ou formation, une vérification se fait par le Vice-Président Administration et Finance chaque trimestre.<br>
        Tout écart sera bien évidemment abouti à l'exclusion définitive.<br>
        Toute absence justifiée ne sera pas prise en compte lors du décompte des absences.<br>
       
        <br>
        <h3>Article 4 : Les dates limites</h3>
        Il est obligatoire de respecter les dates limites imposées par les Vice-Présidents dans les rendus et retours d’informations.<br>
        Une rallonge des échéances ne peut être demandée qu'avant une période déterminée par le Vice-Président.

        <br>
        <h3>Article 5 : Image de marque</h3>
        Un membre se doit obligatoirement de représenter le club ou la fondation de la meilleure façon qui soit et de défendre ses valeurs, et ce pendant les séminaires nationaux, événements, stands, forums et tous types de candidatures internes et externes ainsi que les publications sur les réseaux sociaux qui pouvant nuire directement à l’image du club.
       
       <br>
        <h2 id="S33">Section 3 : Conseil administratif</h2>

        <br>
        <h3>Article 1 : Hiérarchie</h3>
        Sur présentation d'une requête par un ou plusieurs membres demandant l'expulsion ou la suspension d'un membre, le bureau exécutif prendra toute décision qu'il jugera dans l'intérêt du Club.<br>
        Lors du conseil administratif, il faut avoir la hiérarchie indiquée ci-dessous :<br>
        <li>Président</li>
        <li>Vice-Président Resource Humaine</li>
        <li>Vice-Président Administration et Finance</li>
        <li>Vice-Président Evénementiel</li>
        <li>Vice-Président Communication et Marketing</li>

        <br>
        <h3>Article 2 : Engagement</h3>
        Les membres sont impératifs d’avoir le sens de l’engagement et la responsabilité du travail pour faire réussir les événements et atteindre nos objectifs pour mieux servir notre vision.

        <br>
        <h3>Article 3 : Sanction</h3>
        Tout manquement aux principes édictés par le règlement intérieur du club entraîne des sanctions selon le degré de gravité de l’infraction.<br>
        Les sanctions sont: <br>
        <b>1- Avertissement</b><br>
        <b>2- Blâme écrit</b> <br>
        <b>3- Suspension</b> <br>
        <b>4- Exclusion</b> <br>
        La décision du conseil est par vote. Le président du club a le droit à 2 votes et chaque Vice-Président a 1 vote. Le conseil ne sera pas tenu de justifier publiquement sa décision, mais pourra fournir des explications sur demande à la personne concernée.<br>
        Une décision est prise en compte seulement lorsque 50% des votes du conseil administratif sont en faveur de cette décision.

        <br>
        <h3>Article 4 : Composition du conseil disciplinaire</h3>
        Le conseil disciplinaire est composé du tous les membres du bureau exécutif.
      </section>

