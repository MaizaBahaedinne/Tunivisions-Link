        <li> Des journées artistiques ouvertes sous forme de manifestations annuelle </li>
        <li> Des concours pour les jeunes créateurs dans différentes spécialités artistiques </li> 
        <li> Des cérémonies dédiées à détecter de nouveaux talents artistiques </li>
        <li> Des cercles débats mensuels animés par des invités connus </li>

        <br>
        <h3>Article 4 : Evénement divertissement</h3>
        Organiser des événements divertissements pour enrichir l’atmosphère et rayonner tant sur l’aspect académique pour offrir aux jeunes leaders des opportunités de loisirs et qui répondent à leurs besoins.
